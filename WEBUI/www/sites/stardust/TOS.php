     <h1>Land Purchase TOS</h1>
      <p>
        
<p class="newstitle">01. All Land is for rent only as it is considered an Intangible Asset.
<br>
<p class="newstitle">02. All Land rented from will come with full Estate Manager Rights. From here on out all parties renting Land will be known as Estate Owners. </p>
<br>
<p class="newstitle">03. There will be a designated "Mainland" Squared Area from 0,0 to 999,999.</p>
<br>
<p class="newstitle">04. In the designated "Mainland" Squared Area any Estate Owner can not put their region next to any other region within the "Mainland" Area </p>
<br>
<pclass="newstitle">05. Estate Owners that want "Private" Island Area should have their regions placed outside this "Mainland" Squared Area.</p> 
<br>
<p class="newstitle">06. Estate Owners may not place a region next to another "Private" Island region.</p>
<br>
<p class="newstitle">07. Estate Owners have the right to do as they wish on their land as long as it does not violate any Local or Foreign Laws.</p>
<br>
<p class="newstitle">08. Estate Owners can eject, ban, and/or deny access to anyone on their land for any reason at any time.</p> 
</p>
<br>
<p class="newstitle">09. Billing for your region/s will be via PayPal Subscription.</p>
<br>		
<p class="newstitle">10. We will allow a grace period if the Estate Owner contacts us at least 72 hours before region fee's are due. However if there is no contact by the Estate Owner by the due date and time, we reserve the right to remove that region and it will lose any content that was on that region.</p>
<br>
<p class="newstitle">11. Any region that is not paid by their due date may lose any and/or all content on the region/s that are due when we remove them from Virtual Reality. So if you plan on not staying take a copy to your inventory if you have not done so.</p>

